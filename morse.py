from time import sleep

green_led = LED(17)

def process(key):
    if key == '.':
        dot()
    elif key == '-':
        dash()

def dot():
    green_led.on()
    unit()
    green_led.off()
    unit()

def dash():
    green_led.on()
    unit()
    unit()
    unit()
    green_led.off()
    unit()

def unit():
    sleep(0.5)

